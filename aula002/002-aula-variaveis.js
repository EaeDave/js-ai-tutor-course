let personagem = 'Naruto Uzumaki';  // Variáveis são formas de armazenar valores na memória e poder consultá-los de forma rápida.
console.log(personagem);
personagem = 'Kakashi Hatake'  // Valor da variável foi atualizado
console.log(personagem);

const vila = 'Aldeia da Folha';  // Constantes também permite armazenar valores na memória da mesma forma, porém sem poder alterar posteriormente.
console.log(vila);

